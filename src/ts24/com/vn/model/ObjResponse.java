package ts24.com.vn.model;


public class ObjResponse {
	private String code;
	private String des;
	private String desDetail;
	private Object objResponse;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public String getDesDetail() {
		return desDetail;
	}
	public void setDesDetail(String desDetail) {
		this.desDetail = desDetail;
	}
	public Object getObjResponse() {
		return objResponse;
	}
	public void setObjResponse(Object objResponse) {
		this.objResponse = objResponse;
	}
}